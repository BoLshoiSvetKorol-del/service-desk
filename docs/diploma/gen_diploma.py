def _fmt(run, bold=False, size=None, font=None):
    run.bold = bold
    run.font.name = font or FONT_NAME
    run.font.size = size or FONT_SIZE
    r = run._r
    rPr = r.get_or_add_rPr()
    rFonts = OxmlElement("w:rFonts")
    rFonts.set(qn("w:ascii"), font or FONT_NAME)
    rFonts.set(qn("w:hAnsi"), font or FONT_NAME)
    rFonts.set(qn("w:cs"),    font or FONT_NAME)
    rPr.insert(0, rFonts)


def _para_fmt(p, align=WD_ALIGN_PARAGRAPH.JUSTIFY, first_indent=True, space_before=0, space_after=0):
    pf = p.paragraph_format
    pf.alignment       = align
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pf.space_before    = Pt(space_before)
    pf.space_after     = Pt(space_after)
    if first_indent:
        pf.first_line_indent = Cm(1.25)


def body(text, bold=False, align=WD_ALIGN_PARAGRAPH.JUSTIFY, indent=True):
    p = doc.add_paragraph()
    _para_fmt(p, align=align, first_indent=indent)
    run = p.add_run(text)
    _fmt(run, bold=bold)
    return p


